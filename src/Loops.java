public class Loops {
    public static void main(String args[])
    {
        for (int i = 0; i < 10; i++) {
            //simple for
        }
        int i = 0;
        while(i<10)
        {   //simple while
            i++;
        }
        do{
            //simple do while
            i++;
        }while (i<10);
        char [] vowels = {'a','e'};
        for (char item:vowels) {
            //enhanced for
        }
    }
}
